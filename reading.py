def main():
    like = input("Do you like reading? (y/n) ")
    while True:
        if like == 'y':
            print("GENRES:")
            print("- Fantasy (1)\n- Sci-Fi (2)\n- Realistic Fiction (3)\n- Non-Fiction (4)\n- Historical Fiction (5)")
            genre = int(input("According to the key above, what is your favorite genre out of the five given genres? "))

            while genre < 1 or genre > 5:
                genre = int(input("Please choose a number from 1-5 which corresponds to your favorite genre: "))

            print()
            print("Great choice! Here's an interesting book in that genre:")
            print()

            if genre == 1:
                printFantasy()
            elif genre == 2:
                printSciFi()
            elif genre == 3:
                printRealistic()
            elif genre == 4:
                printNonFic()
            elif genre == 5:
                printHistoric()
        else:
            print("HOBBIES:")
            print("- Sports (1)\n- Science (2)\n- Music (3)")
            hobby = int(input("According to the key above, what is your favorite hobby out of the three given hobbies? "))

            while hobby < 1 or hobby > 3:
                hobby = int(input("Please choose a number from 1-3 which corresponds to your favorite hobby: "))
            
            print()
            print("Great choice! Here's an interesting book related to your favorite hobby:")
            print()

            if hobby == 1:
                printSports()
            elif hobby == 2:
                printScience()
            elif hobby == 3:
                printMusic()
        
        again = input("Would you like to try another genre/hobby? (y/n) ")

        print("======================================================")

        if again == 'n':
            break
    print()
